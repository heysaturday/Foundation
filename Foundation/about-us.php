<h2>Who We Are</h2>
<p>
The Onslow Memorial Hospital Foundation, Inc. was founded in September of 1989. We are a nonprofit fund-raising group designed to help elevate the standard of healthcare at Onslow Memorial Hospital. The mission of the Foundation is to generate financial and other public support to meet the hospital’s capital needs and aid in its efforts to serve our community with top-notch, life-saving healthcare. Our vision is to empower OMH to become the hospital of choice in Onslow County.


<h2><br />
  Past Successes</h2>
<p>With the support from community businesses, individuals, and hospital employees, we raised nearly $480,000 in support for Onslow Radiation Oncology.  This new facility allows local cancer patients to receive necessary radiation treatments close to home.  We have also raised over $400,000 in support of Project 2020 which is a campaign for a new patient tower at Onslow Memorial Hospital.  <br><br> In 2015 our goal was to purchase 4 new Panda Infant Warmers for the Nursery and Labor & Delivery.  With the support of our community and the Cannon Foundation, we purchased 11 new Panda Infant Warmers.  Now all the Panda Infant Warmers at OMH are the latest and greatest.  In 2016 our campaign was to provide an extreme makeover to Onslow Diagnostics, the outpatient lab and x-ray at Onslow Memorial Hospital.  Thanks to everyone we raised over $130,000 for Project Healthy Spaces.  With these funds we were able to renovate Onslow Diagnostics and the patient and family waiting areas on the 3rd and 4th floor and ICU.<br><br>
