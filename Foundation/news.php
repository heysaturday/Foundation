           <h2><span class="bold"><img src="images/News043015.jpg" width="225" height="300" align="left" />Donor Spotlight</span></h2>

       <p>We are delighted to name Mr. Louis William Sewell Jr. this year&rsquo;s honorary chair of our 2015 OMH Foundation fundraiser, Disco Fever!   Mr. Sewell became involved with our Foundation fundraising events seven years ago as a major contributor to MASH BASH, a fundraiser in support of Onslow Radiation Oncology.  <br />
</p>
       <p>LaRue Hambrick, the Radiation Oncology Project Chair, fondly remembers asking Louis to pledge $100,000 over a 10 year period.  She remembers Louis saying, &ldquo;Well, LaRue, you don&rsquo;t come in lightly do you?&rdquo;   <br />
         </p>
       <p>Mr. Sewell didn&rsquo;t &ldquo;come in lightly&rdquo; either.  He took his pledge seriously and fulfilled it ahead of schedule. It was this pledge that paved the way for a new radiation oncology building – and paved the way for a series of successful annual fundraisers for the OMH Foundation. <br />
       </p>
       <p>Over the years, Mr. Sewell has always found a way to give back to his community.  He has given freely of his time, money, and resources to many organizations without expecting any recognition.<br />
          </p>
       <p>Mr. Sewell has passed on his community spirit to his children.  His son Billy says, &ldquo;Dad taught me the meaning of giving back.  He has passed this down through generations and I hope that Julie and I will continue doing that with Charles William.&rdquo;<br />
         </p>
       <p>Perhaps Mr. Sewell&rsquo;s passion for supporting the community stems from the family&rsquo;s humble beginnings.  As a boy, Mr. Sewell worked on his family&rsquo;s cattle farm, ran a paper route and worked as a sales clerk for Paul Caps while in high school.  <br />
         </p>
       <p>Mr. Sewell attended East Carolina University and obtained his teaching degree. While teaching, he went back to ECU to obtain his masters degree and then became principal at Northwood Park Middle School. It was during this time that he met his lovely wife, Doris.  After a whirlwind courtship, they were married and two years later blessed with twins, Billy and Paula.  After serving the school system for 14 years, two high school classmates asked him to join them in a business venture which ultimately became Golden Corral.<br />
         </p>
       <p>Although Mr. Sewell is now retired from his business ventures, he continues to stay close to home in support of his community – and his growing number of grandchildren.  Family is important to Mr. Sewell and we would like to thank him for being an integral part of our OMH Foundation Family.  We truly appreciate his generosity!  Thank you, Mr. Louis Sewell, for your support of the OMH Foundation.</p>
