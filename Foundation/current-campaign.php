<h2>2016 Campaign: Project Healthy Spaces</h2>
<p>
<span style="font-weight:bold;">Thanks to the incredible turnout of support,</span> the OMH Foundation was able to raise over $120,000 by “The Roaring Twenties’, Onslow Memorial Hospital’s 8th Annual Fundraiser. The majority of these funds were to support Project Healthy Spaces, our campaign to renovate the patient lobby and waiting areas at Onslow Diagnostics. The Extreme Makeover was a huge success.</p>
<p>The OMH Foundation was able to use the funds from Project Healthy Spaces to purchase new furniture for the two main lobbies and the other patient waiting areas. The new and improved seating is covered in environmentally-friendly fabrics that are easy to clean, significantly reducing the risk of healthcare-acquired infections.</p>
<p>In addition, the new furniture, which has firmer cushions and arm rests, is ergonomically designed to assist people with the transition from sitting to standing. This is especially helpful for the elderly, ill patients, or patients with disabilities or injuries. Onslow Diagnostics also received a new coat of paint along with new pictures. The fresh new color and new additions give Onslow Diagnostics a whole new look. An increasing number of clinical studies show that improved hospital design can reduce stress and anxiety, and have a measurable, significant impact on clinical outcomes, patient costs, and patient satisfaction. HGTV would be proud!
</p>
<p>A big thank you to Paula Garris and all the many volunteers that made this makeover possible. We couldn’t have done it without you. And thanks again to all of our Sponsors and Donors for supporting The Roaring Twenties, Project Healthy Spaces!
</p>

