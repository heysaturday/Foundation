         <h2>Project 317 Bistro</h2>
<p> Project 317 Bistro is our 2016-2017 fundraising campaign to renovate the kitchen and grill areas of our hospital cafeteria. Our goal is always to serve the patient, their family members and loved ones, as well as our own important ‘family’–all our hospital employees. Improving 317 Bistro will directly affect all those we serve, and those who serve us each and every day. The kitchen and grill areas must meet the needs of a diverse population: our patients, their families and loved ones, the members of our community, and all our employees. We prepare more than 100,000 patient meals each year, and feed over 200,000 people each year. Project 317 Bistro will provide new technology and innovative equipment, such as open air coolers, hot and cold bars, and a bigger salad bar which will allow us to provide quick “grab and go” healthy options.</p>

<p><img src="images/Project-317-Bistro_logo_small.jpg" alt="Project 2020" width="275" height="200" align="right" /> By also creating a new workflow pattern and patient ordering system, our 317 Bistro will be able to make healthy and delicious foods available throughout the day and night, even during our busiest hours and cafeteria “off hours.” Through implementing new technological advancements in food preparation, distribution, and safety, we can impact the health and well-being of our most important customers: patients and their families.</p>
